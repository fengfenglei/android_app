public class CouponNum {

    /**
     * notUsedNum : 14
     * alreadyUsedNum : 2
     * expiredNum : 8
     */

    private int notUsedNum;
    private int alreadyUsedNum;
    private int expiredNum;

    public int getNotUsedNum() {
        return notUsedNum;
    }

    public void setNotUsedNum(int notUsedNum) {
        this.notUsedNum = notUsedNum;
    }

    public int getAlreadyUsedNum() {
        return alreadyUsedNum;
    }

    public void setAlreadyUsedNum(int alreadyUsedNum) {
        this.alreadyUsedNum = alreadyUsedNum;
    }

    public int getExpiredNum() {
        return expiredNum;
    }

    public void setExpiredNum(int expiredNum) {
        this.expiredNum = expiredNum;
    }
}
